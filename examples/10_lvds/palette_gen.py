for c in colors:
    # convert to 6 bit colors
    r = c[0]>>2;
    g = c[1]>>2;
    b = c[2]>>2;

    col_val = 0;

    col_val |= (r & 1) << 25;
    r >>= 1;
    col_val |= (r & 1) << 21;
    r >>= 1;
    col_val |= (r & 1) << 17;
    r >>= 1;
    col_val |= (r & 1) << 13;
    r >>= 1;
    col_val |= (r & 1) << 9;
    r >>= 1;
    col_val |= (r & 1) << 5;

    col_val |= (g & 1) << 1;
    g >>= 1;
    col_val |= (g & 1) << 26;
    g >>= 1;
    col_val |= (g & 1) << 22;
    g >>= 1;
    col_val |= (g & 1) << 18;
    g >>= 1;
    col_val |= (g & 1) << 14;
    g >>= 1;
    col_val |= (g & 1) << 10;

    col_val |= (b & 1) << 6;
    b >>= 1;
    col_val |= (b & 1) << 2;
    b >>= 1;
    col_val |= (b & 1) << 27;
    b >>= 1;
    col_val |= (b & 1) << 23;
    b >>= 1;
    col_val |= (b & 1) << 19;
    b >>= 1;
    col_val |= (b & 1) << 15;

    print(str.format('0x{:07X},', col_val))
